SUBROUTINE CONSTR_CELL_RELAX(FCELL)
  USE prec
  REAL(q) FCELL(3,3)

  LOGICAL FILFLG
  INTEGER ICELL(3,3)
  INQUIRE(FILE='OPTCELL',EXIST=FILFLG)
  IF (FILFLG) THEN
      OPEN(67,FILE='OPTCELL',FORM='FORMATTED',STATUS='OLD')
      DO J=1,3
        READ(67,"(3I1)") (ICELL(I,J),I=1,3)
      ENDDO
      CLOSE(67)
      DO J=1,3
      DO I=1,3
        IF (ICELL(I,J)==0) FCELL(I,J)=0.0
      ENDDO
      ENDDO
  ENDIF

  RETURN
END SUBROUTINE
